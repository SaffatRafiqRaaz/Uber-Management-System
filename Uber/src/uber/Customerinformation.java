    private void RefreshActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RefreshActionPerformed
        // TODO add your handling code here:
        //This code snippet first clears the table
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        //Then it fills with data from customers information
        try {
            
            conn = connectdb();
            String query = "select * from customers";
            s = conn.createStatement();
            ResultSet r=s.executeQuery(query);
            while(r.next()){
                int customerid=r.getInt(1);
                String customername=r.getString(2),customercontact=r.getString(4),customerpassword=r.getString(3);
                model=(DefaultTableModel)jTable1.getModel();
                model.addRow(new Object[]{customerid,customername,customerpassword,customercontact});
            }
            conn.close();
        } catch (Exception e) {
        }
    }//GEN-LAST:event_RefreshActionPerformed

