    private void DeleteCustomerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteCustomerActionPerformed
        // TODO add your handling code here:
        //This code deletes the selected customer from the table by matching the customer database id with table id
        try {
            int row=jTable1.getSelectedRow();
            int column=0;
            int temp=(Integer)jTable1.getModel().getValueAt(row, column);
            conn = connectdb();
            String query = "delete from customers where customerid="+""+temp+"";
            
            s = conn.createStatement();
            s.executeUpdate(query);
            
            conn.close();

        } catch (Exception e) {
        }
    }//GEN-LAST:event_DeleteCustomerActionPerformed

